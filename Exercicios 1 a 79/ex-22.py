# 25. Como verificar se um elemento está presente em uma lista em Python?


lista = ["maça", "banana", "uva", "laranja"]
if "uva" in lista:
    print("esta presente")
else:
    print("nao esta presete")

# ou para saber se nao esta presente

lista = ["maça", "banana", "uva", "laranja"]
if "ovo" in lista:
    print("esta presente")
else:
    print("nao esta presete")
    
    
