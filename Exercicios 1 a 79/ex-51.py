# 6. Escreva um programa que solicite ao usuário a quantidade de horas trabalhadas
# em um dia e o valor da hora trabalhada, e exiba o salário diário.

horas_trabalhas = int(input("Informe a hora trabalha: "))
horas_dinheiro = float(input("informe quanto você ganha por hora trabalha => "))
salario_diario = horas_trabalhas * horas_dinheiro
print(salario_diario)