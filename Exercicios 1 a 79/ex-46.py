# 1. Escreva um programa que solicite ao usuário dois números e exiba a soma,
# a subtração, a multiplicação e a divisão dos dois números.

num_1 = int(input("Número 1: "))
num_2 = int(input("Número 2: "))
soma = num_1 + num_2
subtracao = num_1 - num_2
multiplicacao = num_1 * num_2
divisao = num_1 / num_2
print(soma, divisao, multiplicacao, subtracao)